    public ClientGUI(Client client) {
        initComponents();
        this.client = client;
        this.currentSecond = 0;
        this.setLocationRelativeTo(null);
        this.chartPanel.setLayout(new BorderLayout());
        series = new XYSeries("Tiempo entre latidos");
        dataset = new XYSeriesCollection(series);
        chart = ChartFactory.createXYLineChart(
                "Tiempo entre latidos",                     //Title
                "Tiempo", "Separación entre latidos",       //xAxisLabel, yAxisLabel
                dataset,                                    //XYDataset
                PlotOrientation.VERTICAL,                   //PlotOrientation
                false, false, false);                       //Legend, Tooltip, urls
        chartPanelAux = new ChartPanel(chart);
        this.chartPanel.add(chartPanelAux, BorderLayout.CENTER);
        this.repaint();
    }
    
    public void startVInicio(){
        this.vinicio = new VInicio(this, true);
        this.vinicio.pack();
        this.vinicio.setVisible(true);
    }
    
    public void startVRenew(){
        this.vrenew = new VRenew(this, true);
        this.vrenew.pack();
        this.vrenew.setVisible(true);
    }
    
    public void showError(String message){
        System.out.println(message);
        this.verror = new VError(this, true, message);
        this.verror.pack();
        this.verror.setVisible(true);
    }
    
    public void cerrarInicio(){
        this.vinicio.close();
    }
    
    public void cerrarError(){
        this.verror.close();
    }
    
    public void closeApp(){
        this.vinicio.close();
        System.exit(0);
    }
    
    public void subscribe(String server, Integer seconds){
        this.client.setRegistry(server).setSeconds(seconds);
        this.client.subscribe();
    }
    
    public void renew(Integer seconds){
        this.client.setSeconds(seconds);
        this.client.renew();
        this.vrenew.dispose();
    }
    
    public void setInteractiveInit(Boolean bool){
        this.vinicio.setInteractive(bool);
    }
    
    public void receiveMessage(String message){
        this.serverTXTArea.append(message+"\n");
        this.repaint();
    }
    
    public void receiveData(Double data){
        this.currentSecond++;
        series.add(currentSecond.doubleValue(), data);
        this.chartPanel.validate();  
        this.repaint();
    }

